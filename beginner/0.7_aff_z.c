#include <unistd.h>

int		aff_z(char *s)
{
	while (*s)
	{
		if (*s == 'z')
		{
			write(1, "z\n", 2);
			return (0);
		}
		++s;
	}
	write(1, "z\n", 2);
	return (0);
}

int		main(int ac, char **av)
{
	if (ac == 2)
		aff_z(av[1]);
	else
		write(1, "z\n", 2);
	return (0);
}
