#include <unistd.h>

void	aff_a(char *s)
{
	int		a;
	
	a = 0;
	while (*s)
	{
		if (*s == 'a')
			a = 1;
		++s;
	}
	if (a)
		write (1, "a", 1);
}

int		main(int ac, char **av)
{
	if (ac == 2)
		aff_a(av[1]);
	else
		write(1, "a", 1);
	write(1, "\n", 1);
	return (0);
}
