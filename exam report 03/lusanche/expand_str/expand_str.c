#include <unistd.h>

int		main(int ac, char **av)
{
	int p;
	
	if (ac == 2)
	{
		while (*av[1] == ' ' || *av[1] == '\t')
			++av[1];
		p = 0;
		while (*av[1])
		{
			if (*av[1] != ' ' && *av[1] != '\t')
			{
				if (p == 1)
				{
					write(1, " ", 1);
					write(1, " ", 1);
					write(1, " ", 1);
					p = 0;
				}
				write(1, &(*av[1]), 1);
				++av[1];
			}
			else
			{
				++av[1];
				p = 1;
			}
		}
	}
	write(1, "\n", 1);
	return (0);
}
