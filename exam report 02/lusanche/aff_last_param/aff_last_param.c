#include <unistd.h>

int		main(int ac, char **av)
{
	if (ac > 1)
	{
		int i;
		
		i = 1;
		while (ac > i)
			++i;
		i -= 1;
		while (*av[i])
		{
			write(1, &(*av[i]), 1);
			++av[i];
		}
	}
	write(1, "\n", 1);
	return (0);
}
