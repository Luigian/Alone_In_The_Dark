#include <unistd.h>

void	camel_to_snake(char *s)
{
	char	c;
	
	while (*s)
	{
		c = *s;
		if (*s >= 'A' && *s <= 'Z')
		{
			c = *s + 32;
			write(1, "_", 1);
		}
		write(1, &c, 1);
		++s;
	}
}		

int		main(int ac, char **av)
{
	if (ac == 2)
		camel_to_snake(av[1]);
	write(1, "\n", 1);
	return (0);
}
