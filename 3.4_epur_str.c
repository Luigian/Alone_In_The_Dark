#include <unistd.h>

int		main(int ac, char **av)
{
	int sp;
	
	if (ac == 2)
	{
		while (*av[1] == '\t' || *av[1] == ' ')
			++av[1];
		sp = 0;
		while (*av[1])
		{
			if (*av[1] == '\t' || *av[1] == ' ')
				sp = 1;
			if (*av[1] != '\t' && *av[1] != ' ')
			{
				if (sp)
				{
					write(1, " ", 1);
					sp = 0;
				}
				write (1, &(*av[1]), 1);
			}
			++av[1];
		}
	}
	write(1, "\n", 1);
	return (0);
}
