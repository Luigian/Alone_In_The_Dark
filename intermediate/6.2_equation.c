#include <stdio.h>

void    equation(int n)
{
	int		a[10] = {0, 11, 22, 33, 44, 55, 66, 77, 88, 99};
	int		cb[10];
	int		i;

	i = 0;
	while (i < 10)
	{
		cb[i] = n - a[i];
		++i;
	}
	i = 0 ;
	while (i < 10)
	{
		if (cb[i] >= 0 && cb[i] < 100)
		{
			printf("A = %d, B = %d, C = %d\n", i, cb[i] % 10, cb[i] > 9 ? cb[i] / 10 : 0);
		}
		++i;
	}
}

