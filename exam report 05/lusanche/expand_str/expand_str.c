#include <unistd.h>

void	expand_str(char *s)
{
	int		t;
	
	t = 0;
	while (*s)
	{
		while (*s == ' ' || *s == '\t')
			++s;
		while (*s && *s != ' ' && *s != '\t')
		{
			if (t)
				write(1, "   ", 3);			
			write(1, &(*s), 1);
			++s;
			t = 0;
		}
		t = 1;
	}
}

int		main(int ac, char **av)
{
	if (ac == 2)
	{
		expand_str(av[1]);
	}
	write(1, "\n", 1);
	return (0);
}
