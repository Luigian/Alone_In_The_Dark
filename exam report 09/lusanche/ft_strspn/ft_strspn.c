size_t	ft_strspn(const char *s, const char *accept)
{
	int		i;
	int		j;
	
	i = 0;
	j = 0;
	while(s[i] != '\0')
	{
		if (s[i] == accept[j])
		{
			++i;
			j = 0;
		}
		else
			++j;
		if (accept[j] == '\0')
			break ;
	}
	return (i);
}
